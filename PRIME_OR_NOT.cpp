#include<iostream>
using namespace std;
int main(){
    int n,i;
    cout<<"ENTER THE NUMBER TO BE CHECKED: "<<endl;
    cin>>n;
    for(i=2;i<=2;i++)
    {
        if(0!=n%i)
        {
            cout<<"ITS A PRIME NUMBER :)";
        }
        else
        {
            cout<<"ITS NOT A PRIME NUMBER :(";
        }
    }
}